#[derive(Debug)]
enum ShcError {
    Io(io::Error),
    InvalidArgs(String),
    InvalidData(String),
    ProcessFailed(String),
    EarlyExit(i32),
}

impl fmt::Display for ShcError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            ShcError::Io(e) => write!(f, "{e}"),
            ShcError::InvalidArgs(s) => write!(f, "{s}"),
            ShcError::InvalidData(s) => write!(f, "{s}"),
            ShcError::ProcessFailed(s) => write!(f, "{s}"),
            ShcError::EarlyExit(code) => write!(f, "exit({code})"),
        }
    }
}

impl From<io::Error> for ShcError {
    fn from(value: io::Error) -> Self {
        ShcError::Io(value)
    }
}

