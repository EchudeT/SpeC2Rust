#[derive(Clone)]
struct Arc4State {
    indx: u8,
    jndx: u8,
    kndx: u8,
    stte: [u8; 256],
}

impl Default for Arc4State {
    fn default() -> Self {
        let mut s = Self {
            indx: 0,
            jndx: 0,
            kndx: 0,
            stte: [0; 256],
        };
        for (i, b) in s.stte.iter_mut().enumerate() {
            *b = i as u8;
        }
        s
    }
}

