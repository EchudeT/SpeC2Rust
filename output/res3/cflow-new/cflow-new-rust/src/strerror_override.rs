pub struct StrerrorOverride;

impl StrerrorOverride {
    pub fn error_string(errnum: i32) -> Option<&'static str> {
        match errnum {
            0 => Some("Success"),

            x if x == Self::code("EINPROGRESS") => Some("Operation now in progress"),
            x if x == Self::code("EALREADY") => Some("Operation already in progress"),
            x if x == Self::code("ENOTSOCK") => Some("Socket operation on non-socket"),
            x if x == Self::code("EDESTADDRREQ") => Some("Destination address required"),
            x if x == Self::code("EMSGSIZE") => Some("Message too long"),
            x if x == Self::code("EPROTOTYPE") => Some("Protocol wrong type for socket"),
            x if x == Self::code("ENOPROTOOPT") => Some("Protocol not available"),
            x if x == Self::code("EPROTONOSUPPORT") => Some("Protocol not supported"),
            x if x == Self::code("EOPNOTSUPP") => Some("Operation not supported"),
            x if x == Self::code("EAFNOSUPPORT") => Some("Address family not supported by protocol"),
            x if x == Self::code("EADDRINUSE") => Some("Address already in use"),
            x if x == Self::code("EADDRNOTAVAIL") => Some("Cannot assign requested address"),
            x if x == Self::code("ENETDOWN") => Some("Network is down"),
            x if x == Self::code("ENETUNREACH") => Some("Network is unreachable"),
            x if x == Self::code("ECONNRESET") => Some("Connection reset by peer"),
            x if x == Self::code("ENOBUFS") => Some("No buffer space available"),
            x if x == Self::code("EISCONN") => Some("Transport endpoint is already connected"),
            x if x == Self::code("ENOTCONN") => Some("Transport endpoint is not connected"),
            x if x == Self::code("ETIMEDOUT") => Some("Connection timed out"),
            x if x == Self::code("ECONNREFUSED") => Some("Connection refused"),
            x if x == Self::code("ELOOP") => Some("Too many levels of symbolic links"),
            x if x == Self::code("EHOSTUNREACH") => Some("No route to host"),
            x if x == Self::code("EWOULDBLOCK") => Some("Operation would block"),

            x if x == Self::code("ETXTBSY") => Some("Text file busy"),
            x if x == Self::code("ENODATA") => Some("No data available"),
            x if x == Self::code("ENOSR") => Some("Out of streams resources"),
            x if x == Self::code("ENOSTR") => Some("Device not a stream"),
            x if x == Self::code("ETIME") => Some("Timer expired"),
            x if x == Self::code("EOTHER") => Some("Other error"),

            x if x == Self::code("ESOCKTNOSUPPORT") => Some("Socket type not supported"),
            x if x == Self::code("EPFNOSUPPORT") => Some("Protocol family not supported"),
            x if x == Self::code("ESHUTDOWN") => {
                Some("Cannot send after transport endpoint shutdown")
            }
            x if x == Self::code("ETOOMANYREFS") => {
                Some("Too many references: cannot splice")
            }
            x if x == Self::code("EHOSTDOWN") => Some("Host is down"),
            x if x == Self::code("EPROCLIM") => Some("Too many processes"),
            x if x == Self::code("EUSERS") => Some("Too many users"),
            x if x == Self::code("EDQUOT") => Some("Disk quota exceeded"),
            x if x == Self::code("ESTALE") => Some("Stale NFS file handle"),
            x if x == Self::code("EREMOTE") => Some("Object is remote"),

            x if x == 995 => Some("Overlapped operation aborted"),
            x if x == 996 => Some("Overlapped I/O event object not in signaled state"),
            x if x == 997 => Some("Overlapped operations will complete later"),
            x if x == 10091 => Some("Network subsystem is unavailable"),
            x if x == 10092 => Some("Winsock.dll version out of range"),
            x if x == 10093 => Some("Successful WSAStartup not yet performed"),
            x if x == 10101 => Some("Graceful shutdown in progress"),
            x if x == 10102 || x == 11002 => Some("No more results"),
            x if x == 10103 || x == 10111 => Some("Call was canceled"),
            x if x == 10104 => Some("Procedure call table is invalid"),
            x if x == 10105 => Some("Service provider is invalid"),
            x if x == 10106 => Some("Service provider failed to initialize"),
            x if x == 10107 => Some("System call failure"),
            x if x == 10108 => Some("Service not found"),
            x if x == 10109 => Some("Class type not found"),
            x if x == 10112 => Some("Database query was refused"),
            x if x == 11001 => Some("Host not found"),
            x if x == 11002 => Some("Nonauthoritative host not found"),
            x if x == 11003 => Some("Nonrecoverable error"),
            x if x == 11004 => Some("Valid name, no data record of requested type"),

            x if x == Self::code("ENOMSG") => Some("No message of desired type"),
            x if x == Self::code("EIDRM") => Some("Identifier removed"),
            x if x == Self::code("ENOLINK") => Some("Link has been severed"),
            x if x == Self::code("EPROTO") => Some("Protocol error"),
            x if x == Self::code("EMULTIHOP") => Some("Multihop attempted"),
            x if x == Self::code("EBADMSG") => Some("Bad message"),
            x if x == Self::code("EOVERFLOW") => Some("Value too large for defined data type"),
            x if x == Self::code("ENOTSUP") => Some("Not supported"),
            x if x == Self::code("ENETRESET") => Some("Network dropped connection on reset"),
            x if x == Self::code("ECONNABORTED") => Some("Software caused connection abort"),
            x if x == Self::code("ECANCELED") => Some("Operation canceled"),
            x if x == Self::code("EOWNERDEAD") => Some("Owner died"),
            x if x == Self::code("EILSEQ") => {
                Some("Invalid or incomplete multibyte or wide character")
            }

            _ => None,
        }
    }

    fn code(name: &str) -> i32 {
        match name {
            "EINPROGRESS" => 115,
            "EALREADY" => 114,
            "ENOTSOCK" => 88,
            "EDESTADDRREQ" => 89,
            "EMSGSIZE" => 90,
            "EPROTOTYPE" => 91,
            "ENOPROTOOPT" => 92,
            "EPROTONOSUPPORT" => 93,
            "EOPNOTSUPP" => 95,
            "EAFNOSUPPORT" => 97,
            "EADDRINUSE" => 98,
            "EADDRNOTAVAIL" => 99,
            "ENETDOWN" => 100,
            "ENETUNREACH" => 101,
            "ECONNRESET" => 104,
            "ENOBUFS" => 105,
            "EISCONN" => 106,
            "ENOTCONN" => 107,
            "ETIMEDOUT" => 110,
            "ECONNREFUSED" => 111,
            "ELOOP" => 40,
            "EHOSTUNREACH" => 113,
            "EWOULDBLOCK" => 11,
            "ETXTBSY" => 26,
            "ENODATA" => 61,
            "ENOSR" => 63,
            "ENOSTR" => 60,
            "ETIME" => 62,
            "EOTHER" => 131,
            "ESOCKTNOSUPPORT" => 94,
            "EPFNOSUPPORT" => 96,
            "ESHUTDOWN" => 108,
            "ETOOMANYREFS" => 109,
            "EHOSTDOWN" => 112,
            "EPROCLIM" => 67,
            "EUSERS" => 87,
            "EDQUOT" => 122,
            "ESTALE" => 116,
            "EREMOTE" => 66,
            "ENOMSG" => 42,
            "EIDRM" => 43,
            "ENOLINK" => 67,
            "EPROTO" => 71,
            "EMULTIHOP" => 72,
            "EBADMSG" => 74,
            "EOVERFLOW" => 75,
            "ENOTSUP" => 95,
            "ENETRESET" => 102,
            "ECONNABORTED" => 103,
            "ECANCELED" => 125,
            "EOWNERDEAD" => 130,
            "ENOTRECOVERABLE" => 131,
            "EILSEQ" => 84,
            _ => i32::MIN,
        }
    }
}
